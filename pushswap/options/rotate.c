#include "../push_swap.h"

void	rotate(t_stack *stack)
{
	t_node	*first;

	if (!stack || !stack->top || !stack->top->next)
		return ;
	first = stack->top;
	stack->top = first->next;
	stack->bottom->next = first;
	first->next = NULL;
	stack->bottom = first;
}

void	ra(t_stack *a)
{
	rotate(a);
	ft_printf("ra\n");
}

void	rb(t_stack *b)
{
	rotate(b);
	ft_printf("rb\n");
}

void	rr(t_stack *a, t_stack *b)
{
	rotate(a);
	rotate(b);
	ft_printf("rr\n");
}
